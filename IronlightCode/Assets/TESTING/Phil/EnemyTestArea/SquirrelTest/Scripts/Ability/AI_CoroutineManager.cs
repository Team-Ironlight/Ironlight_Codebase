using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public abstract class AI_CoroutineManager: ScriptableObject
{
    public abstract IEnumerator Jump(MonoBehaviour runner, float duration, float minDistanceToAttack, float maxDistanceToAttack);

    public abstract IEnumerator Dash_Coroutine(MonoBehaviour runner, Vector3 source, int x, Vector3 fovline1, Vector3 fovline2, float minDistanceToAttack, float maxDistanceToAttack);

    public abstract IEnumerator Jump_Coroutine(MonoBehaviour runner, float minDistanceToAttack, float maxDistanceToAttack);

    public abstract IEnumerator Swag_Coroutine(MonoBehaviour runner, float minDistanceToAttack, float maxDistanceToAttack);

    public abstract IEnumerator Rotate_Coroutine(MonoBehaviour runner, float minDistanceToAttack, float maxDistanceToAttack, bool isCharging);
}




















































































































































































































































































































































































































































































































