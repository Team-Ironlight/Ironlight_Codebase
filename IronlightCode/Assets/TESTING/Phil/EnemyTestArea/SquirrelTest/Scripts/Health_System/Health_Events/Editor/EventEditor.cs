using UnityEditor;
using UnityEngine;

[CustomEditor(typeof(HealthEvent))]
public class EventEditor : Editor
{
    public override void OnInspectorGUI()
    {
        base.OnInspectorGUI();

        GUI.enabled = Application.isPlaying;

        HealthEvent e = target as HealthEvent;
        if (GUILayout.Button("Raise"))
            e.Raise();
    }
}









































































































































































































































































































































































































































































































