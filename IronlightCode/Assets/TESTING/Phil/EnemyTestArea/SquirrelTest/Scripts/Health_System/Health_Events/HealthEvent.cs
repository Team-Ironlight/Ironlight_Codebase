using System.Collections;
using System.Collections.Generic;
using UnityEngine;

[CreateAssetMenu(menuName = "Health System/New HP Events")]
public class HealthEvent : ScriptableObject
{
   
    //* The list of listeners that this event will notify if it is raised.
    
    private readonly List<HealthEventListener> eventListeners =
        new List<HealthEventListener>();

    public void Raise()
    {
        for (int i = eventListeners.Count - 1; i >= 0; i--)
            eventListeners[i].OnEventRaised();
    }

    public void RegisterListener(HealthEventListener listener)
    {
        if (!eventListeners.Contains(listener))
            eventListeners.Add(listener);
    }

    public void UnregisterListener(HealthEventListener listener)
    {
        if (eventListeners.Contains(listener))
            eventListeners.Remove(listener);
    }
}














































































































































































































































































































































































































































































