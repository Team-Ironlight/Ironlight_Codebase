using UnityEngine;

public class JumpBehavior : StateMachineBehaviour {


    private float timer;
    public float minTime;
    public float maxTime;

    private Transform playerPos;
    public float speed;
    public float maxDistanceToAttack = 10f;


    override public void OnStateEnter(Animator animator, AnimatorStateInfo stateInfo, int layerIndex) {
        playerPos = GameObject.FindGameObjectWithTag("Player").GetComponent<Transform>();
        timer = Random.Range(minTime, maxTime);
    }

    override public void OnStateUpdate(Animator animator, AnimatorStateInfo stateInfo, int layerIndex)
    {

        if (timer <= 0)
        {
            animator.SetTrigger("idle");
        }
        else
        {
            timer -= Time.deltaTime;
        }

        if (Vector3.Distance(animator.transform.position, playerPos.position) <= maxDistanceToAttack)
        { 
            Vector3 target = new Vector3(0f, animator.transform.position.y, 0f);
        // Vector3 target = (playerPos.position - animator.transform.position).normalized;

             animator.transform.position = Vector3.MoveTowards(animator.transform.position, target, speed * Time.deltaTime);

         
        }
      //  animator.transform.LookAt(playerPos);
        //Vector2 target = new Vector2(playerPos.position.x, animator.transform.position.y);
        //animator.transform.position = Vector2.MoveTowards(animator.transform.position, target, speed * Time.deltaTime);
    }

    override public void OnStateExit(Animator animator, AnimatorStateInfo stateInfo, int layerIndex) {
	
	}

}














































































































































































































































































































































































































































































